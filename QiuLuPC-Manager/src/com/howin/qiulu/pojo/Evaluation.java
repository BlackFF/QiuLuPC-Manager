package com.howin.qiulu.pojo;

import java.sql.Timestamp;

/**
 * Evaluation entity. @author MyEclipse Persistence Tools
 */

public class Evaluation implements java.io.Serializable {

	// Fields

	private Integer id;
	private Long orderId;
	private Integer commodityScore;
	private Integer expressScore;
	private Integer skuId;
	private String content;
	private Timestamp time;
	private String image;
	private Integer type;
	// Constructors

	/** default constructor */
	public Evaluation() {
	}

	/** minimal constructor */
	public Evaluation(Long orderId, Integer commodityScore,
			Integer expressScore, Integer skuId) {
		this.orderId = orderId;
		this.commodityScore = commodityScore;
		this.expressScore = expressScore;
		this.skuId = skuId;
	}

	/** full constructor */

	public Evaluation(Integer id, Long orderId, Integer commodityScore, Integer expressScore, Integer skuId,
			String content, Timestamp time, String image,Integer type) {
		super();
		this.id = id;
		this.orderId = orderId;
		this.commodityScore = commodityScore;
		this.expressScore = expressScore;
		this.skuId = skuId;
		this.content = content;
		this.time = time;
		this.image = image;
		this.type=type;
	}

	// Property accessors

	public Integer getId() {
		return this.id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Long getOrderId() {
		return this.orderId;
	}

	public void setOrderId(Long orderId) {
		this.orderId = orderId;
	}

	public Integer getCommodityScore() {
		return this.commodityScore;
	}

	public void setCommodityScore(Integer commodityScore) {
		this.commodityScore = commodityScore;
	}

	public Integer getExpressScore() {
		return this.expressScore;
	}

	public void setExpressScore(Integer expressScore) {
		this.expressScore = expressScore;
	}

	
	public Integer getSkuId() {
		return skuId;
	}

	public void setSkuId(Integer skuId) {
		this.skuId = skuId;
	}

	public String getContent() {
		return this.content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Timestamp getTime() {
		return time;
	}

	public void setTime(Timestamp time) {
		this.time = time;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}
	

}