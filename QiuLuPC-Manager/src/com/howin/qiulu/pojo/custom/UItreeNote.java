package com.howin.qiulu.pojo.custom;

import java.util.List;

public class UItreeNote {

	private Integer id;
	private String text;
	private List<UItreeNote> children;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public List<UItreeNote> getChildren() {
		return children;
	}
	public void setChildren(List<UItreeNote> children) {
		this.children = children;
	}
	
}
